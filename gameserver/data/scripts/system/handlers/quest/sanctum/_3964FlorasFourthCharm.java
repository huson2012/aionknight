package quest.sanctum;


import gameserver.model.gameobjects.player.Player;
import gameserver.quest.handlers.QuestHandler;
import gameserver.quest.model.QuestCookie;
import gameserver.quest.model.QuestState;
import gameserver.quest.model.QuestStatus;


/**
 * @author XRONOS
 *
 */

public class _3964FlorasFourthCharm extends QuestHandler
{
	private final static int	questId	= 3964;
	private final static int[]	npcs = {798384, 203740};
	
	public _3964FlorasFourthCharm()
	{
		super(questId);
	}

	@Override
	public void register()
	{
		qe.setNpcQuestData(798384).addOnQuestStart(questId);
		for(int npc: npcs)
			qe.setNpcQuestData(npc).addOnTalkEvent(questId);
	}

	@Override
	public boolean onDialogEvent(QuestCookie env)
	{
		Player player = env.getPlayer();
		QuestState qs = player.getQuestStateList().getQuestState(questId);
		
		if(defaultQuestNoneDialog(env, 798384, 182206111, 1))
			return true;

		if(qs == null)
			return false;

		int var = qs.getQuestVarById(0);
		
		if(qs.getStatus() == QuestStatus.START)
		{
			switch (env.getTargetId())
			{
				case 203740:
					switch(env.getDialogId())
					{
						case 26:
							if(var == 0)
								return sendQuestDialog(env, 1352);
						case 10000:
							return defaultCloseDialog(env, 0, 1, 0, 0, 182206111, 1);
					}
					break;
				case 798384:
					switch(env.getDialogId())
					{
						case 26:
							if(var == 1)
								return sendQuestDialog(env, 2375);
						case 34:
							return defaultQuestItemCheck(env, 1, 2, true, 5, 2716);
					}
					break;
			}
		}
		return defaultQuestRewardDialog(env, 798384, 0);
	}
}
