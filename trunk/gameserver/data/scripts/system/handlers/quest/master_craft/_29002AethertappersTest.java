package quest.master_craft;

import gameserver.model.gameobjects.player.Player;
import gameserver.quest.handlers.QuestHandler;
import gameserver.quest.model.QuestCookie;
import gameserver.quest.model.QuestState;
import gameserver.quest.model.QuestStatus;

public class _29002AethertappersTest extends QuestHandler
{
	private final static int	questId	= 29002;
	private final static int[]	npc_ids	= {204257, 204099};

	public _29002AethertappersTest()
	{
		super(questId);
	}

	@Override
	public boolean onDialogEvent(QuestCookie env)
	{
		Player player = env.getPlayer();
		
		if(defaultQuestNoneDialog(env, 204257, 4762))
			return true;

		QuestState qs = player.getQuestStateList().getQuestState(questId);
		if(qs == null)
			return false;

		int var = qs.getQuestVarById(0);
		if(qs.getStatus() == QuestStatus.START)
		{
			switch(env.getTargetId())
			{
				case 204099:
					switch(env.getDialogId())
					{
						case 26:
							if(var == 0)
								return sendQuestDialog(env, 1011);
						case 10000:
							return defaultCloseDialog(env, 0, 1, 122001251, 1, 0, 0);
					}
					break;
				case 204257:
					switch(env.getDialogId())
					{
						case 26:
							if(var == 1)
								return sendQuestDialog(env, 1352);
						case 34:
							return defaultQuestItemCheck(env, 1, 2, true, 5, 10001);
					}
					break;
			}
		}
		return defaultQuestRewardDialog(env, 204257, 0);
	}

	@Override
	public void register()
	{
		qe.setNpcQuestData(204257).addOnQuestStart(questId);
		for(int npc_id : npc_ids)
			qe.setNpcQuestData(npc_id).addOnTalkEvent(questId);	
	}
}